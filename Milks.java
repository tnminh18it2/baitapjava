package Text;

public class Milks {
	String masua;
	String tensua;
	int sl;
	String namSX;
public String getMasua() {
	return masua;
}
	public void setMasua(String masua) {
		this.masua = masua;
}
	public String getTensua() {
		return tensua;
}
	public void setTensua(String tensua) {
		this.tensua = tensua;
}
	public int getSl() {
		return sl;
}
	public void setSl(int sl) {
		this.sl = sl;
}
	public String getNamSX() {
		return namSX;
}
	public void setNamSX(String namSX) {
		this.namSX = namSX;
}
public Milks(String masua,String tensua,int sl,String namSX) {
	this.masua=masua;
	this.tensua=tensua;
	this.sl=sl;
	this.namSX=namSX;
}
}
